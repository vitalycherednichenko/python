#Arithmetic operators
a = 10
b = 3
print(a + b)  # 13
print(a // b)  # 3
print(a % b)  # 1
print(a ** b)  # 1000

#Assignment operators
#Comparison operators
x = 5
y = 10
print(x == y)  # False
print(x < y)  # True

#Logical operators
is_sunny = True
is_warm = False
print(is_sunny and is_warm)  # False
print(is_sunny or is_warm)  # True
print(not is_sunny)  # False

